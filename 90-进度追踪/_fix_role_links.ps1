# 处理所有 .md 文件
$files = Get-ChildItem -Path $BASE -Recurse -Filter "*.md" -File
$totalFiles = $files.Count
$changedFiles = 0
$totalReplacements = 0

foreach ($file in $files) {
    $content = Get-Content -Path $file.FullName -Raw -Encoding UTF8
    $originalContent = $content
    $fileReplacements = 0

    foreach ($key in $replacements.Keys) {
        $count = ([regex]::Matches($content, [regex]::Escape($key))).Count
        if ($count -gt 0) {
            $content = $content -replace [regex]::Escape($key), $replacements[$key]
            $fileReplacements += $count
        }
    }

    if ($content -ne $originalContent) {
        Set-Content -Path $file.FullName -Value $content -Encoding UTF8 -NoNewline
        $changedFiles++
        $totalReplacements += $fileReplacements
        Write-Host "[$($file.Name)] $fileReplacements 处替换" -ForegroundColor Green
    }
}

Write-Host ""
Write-Host "=== 修复完成 ===" -ForegroundColor Cyan
Write-Host "扫描文件: $totalFiles"
Write-Host "修改文件: $changedFiles"
Write-Host "总替换数: $totalReplacements"
